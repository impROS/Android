package cale.impros.unittest;

public class Hesaplama {

    public int topla(int a, int b) {
        return a + b;
    }

    public int cikar(int a, int b) {
        return a - b;
    }

    public int bolme(int a, int b) {
        return a / b;
    }

    public int carpma(int a, int b) {
        return a * b;
    }
}
